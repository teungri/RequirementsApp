<br><br><b>Reqheap</b> is a web-based requirement management program.
<br>It allows managing for projects, releases and requirements.
<br><br><b>Feature List</b>:
<ul>
<li>managing of projects, subprojects, releases, test cases, stakeholders, glossaries 
<li>supports different users rights 
<li>rich text edited requirements 
<li>tree structures 
<li>pdf export 
<li>XLS and CSV export 
<li>short and long views of requirements 
<li>historization 
<li>multi language support 
<li>user defined fields per installation 
<li>diagnostics tools 
<li>control panel
<li>statistics 
<li>etc. 
</ul>
